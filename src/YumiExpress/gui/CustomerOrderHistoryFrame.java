        private void loadDetails()
   {
       tableOrder.removeAll();
        
       try{
           
           list=OrderDAO.getOrderDetailsByCustomerID(UserProfile.getUserId(),"DELIVERED");
           Object[]obj=new Object[7];
           for(OrderPojo op : list)
           {
                obj[0]=op.getOrderId();
                obj[1]=op.getProductName();
                obj[2]=op.getProductPrice();
                obj[3]=op.getDeliveryStaffName();
                obj[4]=op.getCompanyName();
                obj[5]=op.getCustomerAddress();
                obj[6]=op.getReview();
                model.addRow(obj);
           }
       }
       catch (SQLException sq) {
            JOptionPane.showMessageDialog(null, "DB ERROR IN OrderFoodFrame");
            sq.printStackTrace();
           } 
   }
   
