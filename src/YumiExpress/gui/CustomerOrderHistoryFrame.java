    private void tableOrderMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableOrderMouseClicked
         int index=tableOrder.getSelectedRow();
          String[] str = {"YES", "NO"};
        int choice = JOptionPane.showOptionDialog(null, "Do you Want to Post Review ?", "Select", JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE, null, str, str[0]);
        if(choice==0)
        {
            String inputValue = JOptionPane.showInputDialog("Please input a Review");
            try{
               boolean result=OrderDAO.updateReview(list.get(index).getOrderId(),inputValue);
               if(result==false)
               {
                    JOptionPane.showMessageDialog(null, "Something went wrong!! while posting review..");
                      return ;
               }
                JOptionPane.showMessageDialog(null, "Review Posted Successfully!!!");
                loadDetails();
            }catch (SQLException sq) {
            JOptionPane.showMessageDialog(null, "DB ERROR IN OrderFoodFrame");
            sq.printStackTrace();
           } 
        }
    }//GEN-LAST:event_tableOrderMouseClicked

